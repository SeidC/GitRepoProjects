#include "result.h"

/*******************************************************************************
 * Result
 ******************************************************************************/
Result::Result(QObject *parent)
    : QObject(parent)
{
    txt  = "";
    size = 0;
}

/*******************************************************************************
 * Result
 ******************************************************************************/
Result::Result(int sSize,QString sTxt, QObject *parent)
    : QObject(parent)
{
    txt  = sTxt;
    size = sSize;

}

/*******************************************************************************
 * QString getTxt
 ******************************************************************************/
QString Result::getTxt() const
{
    return txt;
}

/*******************************************************************************
 * void setTxt()
 ******************************************************************************/
void Result::setTxt(const QString &value)
{
    txt = value;
}

/*******************************************************************************
 * int getSize()
 ******************************************************************************/
int Result::getSize() const
{
    return size;
}

/*******************************************************************************
 * void setSize()
 ******************************************************************************/
void Result::setSize(int value)
{
    size = value;
}
